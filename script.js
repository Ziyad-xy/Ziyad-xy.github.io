function makeBlue() {
    document.body.style.backgroundColor = "#00a8ff";
    document.body.style.color = "#130f40";
}

function makeGreen() {
    document.body.style.backgroundColor = "#178106";
    document.body.style.color = "#000000";
}

function makePink() {
    document.body.style.backgroundColor = "#e84393";
    document.body.style.color = "#b33939";
}